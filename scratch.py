def add_0(x):
    return x + 0


def add_1(x):
    return x + 1


def add_2(x, y):
    return x + y


def add_3(x, y, z):
    return x + y + z


def add_4(x, y, z, a):
    return x + y + z + a


def add_5(x, y, z, a, b):
    return x + y + z + a + b


print(add_0(1))

print(add_1(1))

print(add_2(1, 2))

print(add_3(1, 2, 3))

print(add_4(1, 2, 3, 4))

print(add_5(1, 2, 3, 4, 5))
